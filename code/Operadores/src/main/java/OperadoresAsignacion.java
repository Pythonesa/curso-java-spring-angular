public class OperadoresAsignacion {
    public static void main(String[] args) {
        int a = 10;
        a += 2;
        System.out.println(a);
        a -= 2;
        System.out.println(a);
        a *= 2;
        System.out.println(a);
        a /= 2;
        System.out.println(a);
        a %= 2;
        System.out.println(a);

        String saludo = "Hola ";
        saludo += "Mundo";
        System.out.println(saludo);
    }
}
