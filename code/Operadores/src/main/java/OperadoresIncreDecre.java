public class OperadoresIncreDecre {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        int b = ++a;
        System.out.println(b);
        System.out.println(a);
        int c = a++;
        System.out.println(c);
        System.out.println(a);
    }
}
