public class OperadoresRelacionales {
    public static void main(String[] args) {
        boolean a = 10 > 5;
        System.out.println("10 > 5 = " + a);
        boolean b = 10 < 5;
        System.out.println("10 < 5 = " + b);
        boolean c = 5 >= 5;
        System.out.println("5 >= 5 = " + c);
        boolean d = 10 <= 5;
        System.out.println("10 <= 5 = " + d);
        boolean e = 5 == 5;
        System.out.println("5 == 5 = " + e);
        boolean f = 2 != 5;
        System.out.println("2 != 5 = " + f);
    }
}
